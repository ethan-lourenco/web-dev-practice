// Audio Constructor
let audio = new Audio(); // returns HTMLAudioElement

// Common Methods
audio.play();
audio.pause();
audio.canPlayType();

/* .canPlayType()
   - used to determine if a browser is likely to be able
     to play your specific audio format
*/

/* MIME Type
   - Multipurpose Internet Mail Extensions, is a standardized
     way to programmatically indicate a file type
*/

/* Codec
   - short for "encoder/decoder", is an algorithm or software
     that can convert audio and video between analogue and 
     digital formats
*/

/* HTMLMediaElement API
   - a tool to control the behavior of audio and video elements
     on your page
*/

/* Screen Capture API
   - allows you to record a user's screen
*/
